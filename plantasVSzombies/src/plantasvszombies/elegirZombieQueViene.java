    private void botonElegirZombieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonElegirZombieActionPerformed
        if( Integer.parseInt(textoFilaZombie.getText()) < 0 || Integer.parseInt(textoFilaZombie.getText()) >= 5 )
               JOptionPane.showMessageDialog(this, "¡La fila ha de estar entre 0 y 4!");
        else
        {
            empezarAElegir = true;
            fil = Integer.parseInt(textoFilaZombie.getText());
            botonElegirZombie.setEnabled(false);
            textoFilaZombie.setEnabled(false);
        }
    }//GEN-LAST:event_botonElegirZombieActionPerformed

