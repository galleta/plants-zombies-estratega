    private void panelZombieNormalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelZombieNormalMouseClicked
        if( empezarAElegir )
        {
            boolean encontrado = false;
            ListIterator i = zombs.listIterator();
            while (!encontrado && i.hasNext())
            {
                zombie aux = ((zombie)i.next());
                encontrado = aux.getNombre().equals("Zombie");
            }
            if( encontrado )
            {
                zombi = new zombie("Zombie", 20, 30);
                fil = Integer.parseInt(textoFilaZombie.getText());
                JOptionPane.showMessageDialog(this, "¡Elegido el zombie normal!");
            }
            else
                JOptionPane.showMessageDialog(this, "¡No has elegido antes al zombie normal!");
        }
}//GEN-LAST:event_panelZombieNormalMouseClicked

