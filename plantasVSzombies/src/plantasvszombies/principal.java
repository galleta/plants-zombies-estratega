    private void botonSimularPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSimularPartidaActionPerformed
        if( arrayPlantas.size() > 0 && arrayZombies.size() > 0 )
        {
            Tablero miJardin = new Tablero (arrayPlantas, arrayZombies);
            miJardin.setTitle("Simulación de juego");
            miJardin.setSize(1220, 700);
            miJardin.setVisible(true);
        }
        else
            if(arrayPlantas.size() == 0 && arrayZombies.size() == 0)
                JOptionPane.showMessageDialog(this, "Elige las plantas y los zombies");
            else
                if( arrayPlantas.size() == 0 )
                    JOptionPane.showMessageDialog(this, "Elige las plantas");
                else
                    JOptionPane.showMessageDialog(this, "Elige los zombies");
    }//GEN-LAST:event_botonSimularPartidaActionPerformed

