    private void elegirPlantasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elegirPlantasActionPerformed
        elegirPlantitas ventanaElegirPlantas = null;
        try {
            ventanaElegirPlantas = new elegirPlantitas(arrayPlantas);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedAudioFileException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        ventanaElegirPlantas.setTitle("Elige las plantas! que vienen los zombies!");
        ventanaElegirPlantas.setSize(700,500);
        // ***** Centro la ventana *****
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension frameSize = ventanaElegirPlantas.getSize();
        if (frameSize.height > screenSize.height)
            frameSize.height = screenSize.height;
        if (frameSize.width > screenSize.width)
            frameSize.width = screenSize.width;
        ventanaElegirPlantas.setLocation( (screenSize.width - frameSize.width) / 2, (screenSize.height - frameSize.height) / 2);
        // *****************************
        ventanaElegirPlantas.setVisible(true);
    }//GEN-LAST:event_elegirPlantasActionPerformed

